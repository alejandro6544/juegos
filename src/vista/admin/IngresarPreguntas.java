    private void buscarTrivia() {

        String seleccionado = jComboBox1.getSelectedItem().toString();
        Iterator it = listaTrivias.entrySet().iterator();
        while (it.hasNext()) {
            //if (listaTrivias.containsValue(seleccionado)) {
            Map.Entry pair = (Map.Entry) it.next();
            if (pair.getValue().toString().equals(seleccionado)) {
                idTrivia = (int) pair.getKey();
                //break;
            }
        }
    }
